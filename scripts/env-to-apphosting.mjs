#!/usr/bin/env node
/**
 * Turn KEY=value lines (.env style) into Firebase App Hosting `env:` blocks for apphosting.yaml.
 *
 * Usage:
 *   node scripts/env-to-apphosting.mjs [path/to/.env]
 *   node scripts/env-to-apphosting.mjs --file .env.local
 *   node scripts/env-to-apphosting.mjs --secrets GEMINI_API_KEY,GOOGLE_OAUTH_CLIENT_SECRET,GOOGLE_OAUTH_CLIENT_ID
 *   node scripts/env-to-apphosting.mjs --availability RUNTIME    # default
 *   node scripts/env-to-apphosting.mjs --availability BUILD,RUNTIME
 *   cat .env.local | node scripts/env-to-apphosting.mjs --stdin --secrets GEMINI_API_KEY
 *
 * For variables listed in --secrets, outputs `secret: NAME` (create secrets via
 * `firebase apphosting:secrets:set NAME`). Other keys use `value:` with JSON-escaped strings.
 *
 * Does not read .env.local by default (avoid accidental secret leakage into terminal history).
 * Pass an explicit --file when you mean it.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function parseArgs(argv) {
  const out = {
    file: null,
    stdin: false,
    secrets: new Set(),
    availability: ["RUNTIME"],
    includeEmpty: false,
    help: false,
  };
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--help" || a === "-h") out.help = true;
    else if (a === "--stdin") out.stdin = true;
    else if (a === "--include-empty") out.includeEmpty = true;
    else if (a.startsWith("--file=")) out.file = a.slice("--file=".length);
    else if (a === "--file") out.file = argv[++i] ?? "";
    else if (a.startsWith("--secrets="))
      a
        .slice("--secrets=".length)
        .split(",")
        .map((s) => s.trim())
        .filter(Boolean)
        .forEach((k) => out.secrets.add(k));
    else if (a === "--secrets")
      (argv[++i] ?? "")
        .split(",")
        .map((s) => s.trim())
        .filter(Boolean)
        .forEach((k) => out.secrets.add(k));
    else if (a.startsWith("--availability="))
      out.availability = a
        .slice("--availability=".length)
        .split(",")
        .map((s) => s.trim().toUpperCase())
        .filter(Boolean);
    else if (a === "--availability")
      out.availability = (argv[++i] ?? "RUNTIME")
        .split(",")
        .map((s) => s.trim().toUpperCase())
        .filter(Boolean);
    else if (!a.startsWith("-") && !out.file && !out.stdin) out.file = a;
  }
  return out;
}

function parseDotEnv(text) {
  /** @type {Array<{ key: string; value: string }>} */
  const pairs = [];
  const lines = text.split(/\r?\n/);
  for (let raw of lines) {
    const line = raw.trim();
    if (!line || line.startsWith("#")) continue;
    let s = line;
    if (s.startsWith("export ")) s = s.slice(7).trim();
    const eq = s.indexOf("=");
    if (eq <= 0) continue;
    const key = s.slice(0, eq).trim();
    let value = s.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    pairs.push({ key, value });
  }
  return pairs;
}

function yamlAvailability(av) {
  const lines = ["    availability:"];
  for (const a of av) lines.push(`      - ${a}`);
  return lines.join("\n");
}

function emitEntry(key, value, isSecret, availability) {
  const lines = [`  - variable: ${key}`];
  if (isSecret) {
    lines.push(`    secret: ${key}`);
  } else {
    lines.push(`    value: ${JSON.stringify(value)}`);
  }
  lines.push(yamlAvailability(availability));
  return lines.join("\n");
}

function main() {
  const args = parseArgs(process.argv);
  if (args.help) {
    console.log(`
Usage: node scripts/env-to-apphosting.mjs [--file path] [--stdin] [--secrets K1,K2] [--availability RUNTIME|BUILD|BUILD,RUNTIME]

Examples:
  node scripts/env-to-apphosting.mjs --file .env.example
  node scripts/env-to-apphosting.mjs --file .env.local --secrets GEMINI_API_KEY,GOOGLE_OAUTH_CLIENT_SECRET
`);
    process.exit(0);
  }

  let text = "";
  if (args.stdin) {
    text = fs.readFileSync(0, "utf8");
  } else {
    const filePath = args.file
      ? path.resolve(process.cwd(), args.file)
      : path.join(__dirname, "..", ".env.example");
    if (!fs.existsSync(filePath)) {
      console.error(`File not found: ${filePath}`);
      process.exit(1);
    }
    text = fs.readFileSync(filePath, "utf8");
  }

  const pairs = parseDotEnv(text);
  const av = args.availability.length ? args.availability : ["RUNTIME"];

  const blocks = [];
  blocks.push("# Paste under `env:` in apphosting.yaml (or merge with existing env list).");
  blocks.push("# Generated by: scripts/env-to-apphosting.mjs");
  blocks.push("");

  const emitted = new Set();

  for (const { key, value } of pairs) {
    if (!key) continue;
    const isSecret = args.secrets.has(key);
    if (!args.includeEmpty && value === "" && !isSecret) continue;
    // Secret-backed vars: emit `secret:` even when .env value is empty (real value lives in Secret Manager).
    if (!args.includeEmpty && value === "" && isSecret) {
      blocks.push(emitEntry(key, "", true, av));
      blocks.push("");
      emitted.add(key);
      continue;
    }
    blocks.push(emitEntry(key, value, isSecret, av));
    blocks.push("");
    emitted.add(key);
  }

  for (const key of args.secrets) {
    if (emitted.has(key)) continue;
    blocks.push(emitEntry(key, "", true, av));
    blocks.push("");
  }

  process.stdout.write(blocks.join("\n").replace(/\n+$/, "\n"));
}

main();
